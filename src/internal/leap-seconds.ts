import fs from 'fs';

export async function main(): Promise<number> {
    const listing = fs.readFileSync(0, 'utf-8');
    const lines = listing.trim().split('\n').map(s => s.trim());

    const expirationStamp = lines.find(v => v.startsWith("#@"));
    const lastUpdateStamp = lines.find(v => v.startsWith("#$"));

    if (expirationStamp === undefined) {
        throw new Error("no expiration stamp found")
    }

    if (lastUpdateStamp === undefined) {
        throw new Error("no last update stamp found")
    }

    // Strip comments and filter out empty lines
    const leapLines = lines.map(v => v.substring(0, v.indexOf("#")).trim()).filter(v => v.length > 0);

    const leaps: { stamp: number, delta: number }[] = [];
    for (const leapLine of leapLines) {
        const splitLeap = leapLine.split(/\s+/, 2);
        if (splitLeap.length != 2) {
            throw new Error(`invalid leap line '${leapLine}', expected two groups, ${JSON.stringify(splitLeap)}`)
        }

        const [ntp, dtai] = splitLeap.map(v => v.trim());
        leaps.push({ stamp: parseInt(ntp), delta: parseInt(dtai) });
    }

    const langOutput = process.argv[2];
    switch (langOutput) {
        case "go":
            process.stdout.write(`// Code generated by leap-seconds go. DO NOT EDIT.
package timeconv

const (
	// NTP Timestamp of when this listing was last updated from IERS Bulletin C
	lastUpdateStamp NTP = ${expirationStamp.substring(2).trim()}

	// Expiration date of the file given on a semi-annual basis: last June or last December
	// Hermes should update this using 'yarn leap-seconds'
	expirationStamp NTP = ${expirationStamp.substring(2).trim()}
)

type leapSecond struct {
	// Leap second event timestamp
	Stamp NTP

	// DTAI: The difference DTAI= TAI-UTC in units of seconds
	// It is the quantity to add to UTC to get the time in TAI
	Delta int64
}

var leapSeconds = []leapSecond{
${leaps.sort((a, b) => b.stamp - a.stamp).map(v => `	{Stamp: ${v.stamp}, Delta: ${v.delta}},`).join("\n")}
}
`)
            break;
        case "json":
        default:
            process.stdout.write(JSON.stringify({
                expiration_stamp: parseInt(expirationStamp.substring(2).trim()),
                last_update_stamp: parseInt(lastUpdateStamp.substring(2).trim()),
                leaps
            }, undefined, 4));
            break;
    }

    return 0;
}

process.on('uncaughtException', function (err) {
    console.error(err);
});

main();