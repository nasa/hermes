import { Command } from '@commander-js/extra-typings';
import fs from 'fs';

import * as Hermes from '@gov.nasa.jpl.hermes/types';

function toPascalCase(str: string) {
    return (str.match(/[a-zA-Z0-9]+/g) || []).map(w => `${w.charAt(0).toUpperCase()}${w.slice(1)}`).join('');
}

function typeName(tlm: Hermes.Def.Type): string {
    switch (tlm.kind) {
        case Hermes.Def.TypeKind.reference:
            return toPascalCase(tlm.name);
        case Hermes.Def.TypeKind.u8:
        case Hermes.Def.TypeKind.i8:
        case Hermes.Def.TypeKind.u16:
        case Hermes.Def.TypeKind.i16:
        case Hermes.Def.TypeKind.u32:
        case Hermes.Def.TypeKind.i32:
        case Hermes.Def.TypeKind.u64:
        case Hermes.Def.TypeKind.i64:
        case Hermes.Def.TypeKind.f32:
        case Hermes.Def.TypeKind.f64:
            return Hermes.typeKindName(tlm.kind);
        case Hermes.Def.TypeKind.boolean:
            return "U8";
        case Hermes.Def.TypeKind.string: {
            const lenName = Hermes.typeKindName((tlm.lengthType ?? Hermes.Def.TypeKind.u32));
            return `[${lenName}]string`;
        }
        case Hermes.Def.TypeKind.enum:
        case Hermes.Def.TypeKind.bitmask:
        case Hermes.Def.TypeKind.object:
            return toPascalCase(tlm.name!);
        case Hermes.Def.TypeKind.array:
            if (typeof tlm.size === "number") {
                // Static array
                return `[${tlm.size}]${typeName(tlm.type)}`;
            } else {
                // Dynamic arrays
                const lenName = Hermes.typeKindName((tlm.lengthType ?? Hermes.Def.TypeKind.u32));
                return `[${lenName}]${typeName(tlm.type)}`;
            }
        case Hermes.Def.TypeKind.bytes:
            if (typeof tlm.size === "number") {
                // Static array
                return `[${tlm.size}]${Hermes.typeKindName(tlm.type)}`;
            } else {
                // Dynamic arrays
                const lenName = Hermes.typeKindName((tlm.lengthType ?? Hermes.Def.TypeKind.u32));
                return `[${lenName}]${Hermes.typeKindName(tlm.type)}`;
            }
        case Hermes.Def.TypeKind.void:
            return `[${tlm.size ?? 0}]byte`;
    }
}

function addCodegen(type: Hermes.Def.Type, bigEndian: boolean, codegen: string[]) {
    switch (type.kind) {
        case Hermes.Def.TypeKind.reference:
        case Hermes.Def.TypeKind.u8:
        case Hermes.Def.TypeKind.i8:
        case Hermes.Def.TypeKind.u16:
        case Hermes.Def.TypeKind.i16:
        case Hermes.Def.TypeKind.u32:
        case Hermes.Def.TypeKind.i32:
        case Hermes.Def.TypeKind.u64:
        case Hermes.Def.TypeKind.i64:
        case Hermes.Def.TypeKind.f32:
        case Hermes.Def.TypeKind.f64:
        case Hermes.Def.TypeKind.boolean:
        case Hermes.Def.TypeKind.void:
        case Hermes.Def.TypeKind.array:
        case Hermes.Def.TypeKind.bytes:
        case Hermes.Def.TypeKind.string:
            // No codegen needed
            break;
        case Hermes.Def.TypeKind.enum: {
            codegen.push(`enum ${toPascalCase(type.name)} ${Hermes.typeKindName(type.encodeType ?? Hermes.Def.TypeKind.i32)} {`);
            for (const [name, item] of type.values.entries()) {
                codegen.push(`    ${toPascalCase(name)} = ${item.value}`);
            }
            codegen.push("}");
            codegen.push('');
            break;
        }
        case Hermes.Def.TypeKind.bitmask:
            throw new Error("bitmasks are not supported yet");

        case Hermes.Def.TypeKind.object:
            codegen.push(`struct ${toPascalCase(type.name!)} ${bigEndian ? '*' : '.'} {`);
            for (const field of type.fields) {
                codegen.push(`    ${toPascalCase(field.name)}: ${typeName(field.type)}`);
            }
            codegen.push("}");
            codegen.push('');
        }

}

export async function main(argv?: readonly string[]): Promise<number> {

    const program = new Command();
    program
        .name('dictionary-to-protocol')
        .description(`Convert telemetry from .dict.pb file to a .protocol file`);

    program
        .arguments('<dictionary>')
        .option('--namespace <namespace>', 'Dictionary namespace', "")
        .option('--little-endian', 'Use little endian encoding instead of big endian', false)
        .action(async (dictionary, { namespace, littleEndian }) => {
            const dict = Hermes.Dictionary.fromProto(
                Hermes.Proto.Dictionary.decode(fs.readFileSync(dictionary))
            );

            const ns = dict.get(namespace);
            const code: string[] = [
                "# Code generated by dictionary-to-protocol. DO NOT EDIT.",
                ''
            ];
            for (const [name, type] of ns.getTypes()) {
                type.name = name;
                addCodegen(type, !littleEndian, code);
            }

            process.stdout.write(code.join('\n'));
        });

    await program.parseAsync(argv);
    return 0;
}

main();